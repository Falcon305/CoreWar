#include "libft.h"

int		ft_is_prime(int nb)
{
	int modu;

	modu = nb - 1;
	if (nb <= 1)
		return (0);
	while (modu > 1)
	{
		if ((nb % modu) == 0)
			return (0);
		modu--;
	}
	return (1);
}
